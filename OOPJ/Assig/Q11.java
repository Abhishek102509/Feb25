 //11. Write a program to check if a number is positive and even using logical operators (&&, ||, !).
 
class Q11{
	public static void main(String arg[]){
		int num1 = 10;
        int num2 = 30;
		//logical operators
		System.out.println((num1<num2)&&(num1>num2));
		System.out.println((num1<num2)||(num1>num2));
		System.out.println(!(num1>num2));
		
		
	
	}
}