//10. Write a program to compare two integers using all relational operators (==, !=, >, <, >=, <=) and display the results.


class Q10{
	public static void main(String arg[]){
		int num1 = 10;
        int num2 = 30;
		//relational operators
		System.out.println( num1==num2);
		System.out.println( num1!=num2);
		System.out.println( num1>num2);
		System.out.println( num1<num2);
		System.out.println( num1>=num2);
		System.out.println( num1<=num2);
		
	
	}
}